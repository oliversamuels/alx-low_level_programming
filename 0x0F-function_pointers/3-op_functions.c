#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"

/**
  * op_add - Entry Point
  * @a: Input
  * @b: Input
  * Return: Always 0
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - Entry Point
  * @a: Input
  * @b: Input
  * Return: Always 0
  */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - Entry Point
  * @a: Input
  * @b: Input
  * Return: Always
  */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - Entry Point
  * @a: Input
  * @b: Input
  * Return: Always 0
  */

int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - Entry Point
  * @a: Input
  * @b: Input
  * Return: Always 0
  */

int op_mod(int a, int b)
{
	return (a % b);
}
